int solve()
{
    int n, num;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    int sa = INF; // smallest seen
    int sb = INF; // smallest winning position seen
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        // A: at position smaller than all to left: instant win
        // B: at position where all to left + smaller are type A: instant loss
        // C: at position right and bigger than type B: win by sending to smallest type B
        // Count type B- those not new smallest BUT have no smaller B to left
        if (a[i] < sa)
        {
            sa = a[i];
        }
        else if (a[i] < sb)
        {
            ans++;
            sb = a[i];
        }
    }

    return ans;
}

