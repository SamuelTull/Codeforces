void solve()
{
    vector<int> a(1);
    cin >> a[0];
    while (__builtin_popcount(a.back()) != 1)
    {
        int d = 1 << __builtin_ctzll(a.back());
        a.push_back(a.back() - d);
    }
    while (a.back() != 1)
    {
        int d = 1 << (__builtin_ctzll(a.back()) - 1);
        a.push_back(a.back() - d);
    }
    cout << a.size() << "\n";
    for (int x : a)
        cout << x << " ";
    cout << "\n";
}

